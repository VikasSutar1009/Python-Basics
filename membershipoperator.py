#Example with a list

fruits=["apple","banana", "cherry"]

print("banana" in fruits)   #True, because'banana' is in the list
print("mango" in fruits)    #False, because 'mango' is not in list
print("apple" not in fruits)    #False, because 'apple' is not in the list
print("grape" not in  fruits)    #True, because 'grape' is not in list

#Example with string
text="Hello World"

print("Hello" in text)  #True
print("world" in text)  #False(case-sensitive)
print("Python"not in text)  #true
